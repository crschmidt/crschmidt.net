A sphere with three light sources -- a Red, Blue, and Yellow -- positioned over a black and white checkerboard plane. This piece is taken in large part from the <a href="http://library.thinkquest.org/3285/">POVRay tutorial</a>. I liked the look of the colored shadows -- the mixing of the primary colors in each of the shadows resulted in the purple/orange/green shadows, even though the light sources are all primary colors. The sphere is white, with a high phong value (sheen) and relatively high reflectivity.  
