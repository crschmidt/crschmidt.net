<h2>Summary</h2>
<p>Ever since Google first 'solved' interactive web-based mapping for the masses, providing an API to put your data on top of, people have explored the space at an
   ever increasing rate. From Google Maps Mashups to all new datasets created out of the existing data, and hacked into that ever-lovable interactive mapping interface, 
   people are creating new, exciting, and sometimes even innovative ways of looking at existing data.</p>
 <p> Come discuss the things you've done with maps, the things you're doing with maps, and what you think of the 'mapping revolution'. See what alternatives there are to Google... and why you might not want to be quite so dependant on the corporate data silos.</p>

   
